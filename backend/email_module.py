import socket
socket.setdefaulttimeout(10)
from datetime import datetime
import resend
import os

resend.api_key = os.getenv('RESEND_API_KEY')

def send_dmca_email(report_data):
    """
    Sends a DMCA takedown simulation email via Resend (HTTP API, no SMTP).
    """

    DUMMY_EMAIL = "maria.amir.tech@gmail.com"

    platform = report_data.get('platform', 'Unknown Platform')
    report_id = report_data.get('report_id', 'N/A')
    category = report_data.get('category', 'N/A')
    description = report_data.get('description', 'N/A')
    token = report_data.get('token', 'N/A')
    manipulation_score = report_data.get('manipulation_score', 'N/A')
    verdict = report_data.get('verdict', 'N/A')
    date = datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')

    subject = f"[ShieldNet] DMCA Takedown Request — Case #{report_id} — {platform}"

    body_html = f"""
    <h2>DMCA TAKEDOWN NOTICE</h2>
    <p>Sent via ShieldNet — Cyber Harassment Reporting Platform<br>
    University of Lahore, IET Department</p>
    <hr>
    <p><b>Date:</b> {date}<br>
    <b>Case ID:</b> {report_id}<br>
    <b>Tracking Token:</b> {token}</p>
    <hr>
    <p><b>PLATFORM:</b> {platform}<br>
    <b>VIOLATION CATEGORY:</b> {category}<br>
    <b>DESCRIPTION:</b><br>{description}</p>
    <hr>
    <p><b>AI ANALYSIS RESULTS:</b><br>
    Manipulation Score: {manipulation_score}%<br>
    Verdict: {verdict}</p>
    <hr>
    <p>Under the Digital Millennium Copyright Act (DMCA), I hereby formally
    request the immediate removal of the content described above from {platform}.
    The reported content constitutes cyber harassment and/or unauthorized
    use of personal images/content.</p>
    <p><i>This is a simulation complaint generated by ShieldNet FYP.</i></p>
    <hr>
    <p>ShieldNet | University of Lahore IET<br>
    Supervisor: Sir Abdul Malik<br>
    Generated: {date}</p>
    """

    try:
        result = resend.Emails.send({
            "from": "ShieldNet <onboarding@resend.dev>",
            "to": [DUMMY_EMAIL],
            "subject": subject,
            "html": body_html
        })
        return {'success': True, 'message': f'DMCA email sent to {DUMMY_EMAIL}', 'id': result.get('id')}

    except Exception as e:
        return {'success': False, 'error': str(e)}